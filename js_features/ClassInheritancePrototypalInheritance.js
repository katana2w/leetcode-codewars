/*
*Class Inheritance: A constructor function instantiates an instance via the "new" keyword.
* This new instance inherits properties from a parent class.
*
* Prototypal Inheritance: An instance is created by cloning an existing object that serves as a prototype.
* Instances are typically instantiated via factory functions, object literals, or Object.create().
* Instances may be composed from many different source objects, allowing for easy selective inheritance.
*
*
*
* Every JavaScript function has a prototype property (by default this property is null),
* that is mainly used for implementing inheritance. We add methods and properties to a function's prototype
* so that it becomes available to instances of that function.
* */